public class ConversaoDeUnidadesDeVolume {

    public static double litroParaCm3s(double litro){
        return litro * 1000;

    }
    public static double metro3ParaLitros(double metro3){
        return metro3 * 1000;
    }
    public static double metro3ParaPe3s(double metro3){
        return metro3 * 35.32;
    }
    public static double galaoParaPol3s(double galao){
        return galao * 231;
    }
    public static double galaoParaLitros(double galao){
        return galao * 3.785;
    }
    
}
