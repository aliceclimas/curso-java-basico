public class ConversaoDeUnidadesDeArea {

    public static double metroParaPes(double metro){
        return metro * 10.76;

    }
    public static double peParaCmQuadrados(double pe){
        return pe * 929;
    }
    public static double milhaQuadradosParaAcres(double milha){
        return milha * 640;
    }
    public static double acreParaPesQuadrados(double acre){
        return acre * 43.560;
    }
    
}
